#4a - create empty dictionary
d1 = {}
print("\nEmpty Dictionary: ")
print(d1)

#4b - dictionary with use of Integer keys
d1 = {1: 'COMP', 2: '2150', 3:' python'}
print("\nDictionary with the use of Integer Keys: ")
print(d1)

#4c - create a dictionary with mixed keys
d1 = {'name': 'COMP', 1:[1,2,3,4]}
print("\nDictionary with Mixed Keys: ")
print(d1)

#4d - create a dictionary using the build-in dict() method
d1 = dict({1: 'COMP', 2: '2150', 3: 'python'})
print("\nDictionary using dict(): ")
print(d1)

#4e - create a dictionary with each item paired. (using dict())
d1 = dict([(1, 'COMP'), (2, '2150'), (3, 'python')])
print("\nDictionary with each item as a pair: ")
print(d1)